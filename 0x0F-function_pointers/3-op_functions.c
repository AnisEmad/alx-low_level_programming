#include "3-calc.h"
/**
  * op_add - add two numbers
  * @a: 1st number
  * @b: 2nd number
  * Return: the sum of them
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - subtracte two numbers
  * @a: 1st number
  * @b: 2nd number
  * Return: the subtraction of them
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiply two numbers
  * @a: 1st number
  * @b: 2nd number
  * Return: the mutiplcation of them
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divide two numbers
  * @a: 1st number
  * @b: 2nd number
  * Return: the divison of them
  */
int op_div(int a, int b)
{
	return (a / b);
}
/** 
  * op_mod - compute the modulo 
  * @a: 1st number
  * @b: 2nd number
  * Return: modulo 
  */
int op_mod(int a, int b)
{
	return (a % b);
}
